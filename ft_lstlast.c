#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int	i;

	i = 0;
	if (!lst)
		return (0);
	while (lst->next != 0)
	{
		i++;
		lst = lst->next;
	}
	return (lst);
}
